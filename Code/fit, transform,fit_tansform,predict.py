# Difference between fit and transform , fit_transform and predict
# fit: It is used to learn the parameters of the model from the training data.It is used on training data.
# predict : It is used to predict the output for the given input.
# fit_transform : It is used to learn the parameters of the model from the training data and then transform the training data.It is used on training data.
# transform : It is used to transform the training data.It is used on test data and can separately used on training data. as well
